    struct Hit
    {
        u32 triangleI;
        f32 t;
    };
    auto const closestHit = [triangle](Ray const ray) noexcept
        -> std::optional<Hit>
    {
        auto const intersect = [ray](Triangle const &s) noexcept {return rayTriangleIntersection(ray, s);};
        auto const intersectionR = triangle | std::views::transform(intersect);
        auto const bad = [](RayPoint const r) noexcept {return !std::isfinite(r.tMin);};
        auto const less = [=](RayPoint const r1, RayPoint const r2) noexcept
        {
            if(bad(r1))
                return false;
            if(bad(r2))
                return true;
            return r1.tMin < r2.tMin;
        };
        auto const closestI = std::ranges::min_element(intersectionR, less);
        if(bad(*closestI)) {
            return std::nullopt;
        }
        return Hit
        {
            u32(closestI - std::ranges::begin(intersectionR)),
            (*closestI).tMin
        };
    };

